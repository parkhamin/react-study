// 복합 할당 연산자(Compout assignment operators)
// 다음 두 줄은 같습니다
x = x + 1;
x += 1;

// 다음 두 줄은 같습니다
x = x + 2;
x += 2;

// 다음 두 줄은 같습니다
x = x * 2;
x *= 2;

// 다음 두 줄은 같습니다
x = x - 3;
x -= 3;

// 다음 두 줄은 같습니다
x = x / 2;
x /= 2;

// 다음 두 줄은 같습니다
x = x % 7;
x %= 7;

// 증가, 감소 연산자
// 다음 세 줄은 같은 의미입니다
x = x + 1;
x += 1;
x++;

// 다음 세 줄은 같은 의미입니다
x = x - 1;
x -= 1;
x--;
